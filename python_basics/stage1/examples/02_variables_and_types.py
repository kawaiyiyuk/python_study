# 变量与数据类型示例

# 变量定义
name = "张三"
age = 18
height = 1.75
is_student = True

# 类型转换
age_str = str(age)
height_int = int(height)

# 打印变量类型
print(type(name))    # <class 'str'>
print(type(age))     # <class 'int'>
print(type(height))  # <class 'float'>
print(type(is_student))  # <class 'bool'>

# 打印变量值
print(f"姓名: {name}")
print(f"年龄: {age}")
print(f"身高: {height}")
print(f"是否学生: {is_student}") 