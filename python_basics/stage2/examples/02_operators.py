# 运算符示例

# 算术运算符
a = 10
b = 3
print(f"{a} + {b} = {a + b}")   # 加法
print(f"{a} - {b} = {a - b}")   # 减法
print(f"{a} * {b} = {a * b}")   # 乘法
print(f"{a} / {b} = {a / b}")   # 除法
print(f"{a} // {b} = {a // b}") # 整除
print(f"{a} % {b} = {a % b}")   # 取余
print(f"{a} ** {b} = {a ** b}") # 幂运算

# 比较运算符
print(f"{a} > {b}: {a > b}")    # 大于
print(f"{a} < {b}: {a < b}")    # 小于
print(f"{a} == {b}: {a == b}")  # 等于
print(f"{a} != {b}: {a != b}")  # 不等于

# 逻辑运算符
x = True
y = False
print(f"{x} and {y}: {x and y}") # 与
print(f"{x} or {y}: {x or y}")   # 或
print(f"not {x}: {not x}")       # 非

# 赋值运算符
c = 5
c += 3  # 等价于 c = c + 3
print(f"c += 3: {c}")
c *= 2  # 等价于 c = c * 2
print(f"c *= 2: {c}") 